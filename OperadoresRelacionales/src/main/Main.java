package main;

public class Main {

	public static void main(String[] args) {
		// Operadores Relacionales
		
		int v1 = 200, v2 = 200;
		boolean r;
		
		r = v1 < v2;
		System.out.println(r);
		
		r = v1 > v2;
		System.out.println(r);
		
		r = v1 <= v2;
		System.out.println(r);
		
		r = v1 >= v2;
		System.out.println(r);
		
		r = v1 == v2;
		System.out.println(r);
		
		r = v1 != v2;
		System.out.println(r);

	}

}
