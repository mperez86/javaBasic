package main;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		// Operadores aritméticos
		
		int x = 50, y = 10;
		int result;
		
		result = x + y;
		System.out.println(result);
		
		result = x - y;
		System.out.println(result);
		
		result = x * y;
		System.out.println(result);
		
		result = x / y;
		System.out.println(result);
		
		// ++ incrementa en uno
		// -- decrementa en uno
		x++;
		System.out.println(x);
		
		y += 100;
		System.out.println(y);
		
		x *= 2;
		System.out.println(x);


	}

}
