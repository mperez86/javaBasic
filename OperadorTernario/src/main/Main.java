package main;

public class Main {

	public static void main(String[] args) {
		// Operador ternario
		// IFTTT
		// expresion1 ? expresion2 : expresion3
		
		String mensaje = 10 > 9 ? "Es mayor" : "Es menor";
		System.out.println(mensaje);
		
		int entero = true ? 1 : 0;
		System.out.println(entero);
	}

}
